package main

import "fmt"

func main() {
	// var x string = "hello"
	var x = "golang"
	fmt.Println(x)
	var isAdult = true
	fmt.Println(isAdult)
	var age int = 30
	fmt.Println(age)
	// shorthand syntax
	name := "golang"
	fmt.Println(name)
	var y string
	fmt.Println(y)
	y = "hello"
	fmt.Println(y)
	var price float32 = 50.5
	fmt.Println(price)

}
